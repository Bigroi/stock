package com.bigroi.stock.messager;

public class MailManagerException extends Exception {

	private static final long serialVersionUID = 6501328160211456568L;

	public MailManagerException() {
	}

	public MailManagerException(String arg0) {
		super(arg0);
	}

	public MailManagerException(Throwable arg0) {
		super(arg0);
	}

	public MailManagerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
