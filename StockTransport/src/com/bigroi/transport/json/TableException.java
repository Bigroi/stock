package com.bigroi.transport.json;

public class TableException extends Exception {

	private static final long serialVersionUID = 8253635948034869466L;

	public TableException() {
	}

	public TableException(String arg0) {
		super(arg0);
	}

	public TableException(Throwable arg0) {
		super(arg0);
	}

	public TableException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
