package com.bigroi.stock.docs;

public class DocumentException extends Exception {

	private static final long serialVersionUID = -8457884596616689799L;

	public DocumentException() {
	}

	public DocumentException(String arg0) {
		super(arg0);
	}

	public DocumentException(Throwable arg0) {
		super(arg0);
	}

	public DocumentException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
